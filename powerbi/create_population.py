def esc(v):
    return v.replace('"', '""')


def main():
    srv, model = s._connect_tom()
    if not model:
        print("Power BI is not open"); return
    try:
        reg = s._find_table(model, "regions")

        # SWITCH expression
        pairs = "".join(f'    "{esc(k)}", {v},\n' for k, v in POP.items())
        expr = f"SWITCH ( regions[region_ru],\n{pairs}    BLANK () )"

        # remove the old Population column, if present
        for c in list(reg.Columns):
            if c.Name == "Population":
                reg.Columns.Remove(c)
        col = CalculatedColumn()
        col.Name = "Population"
        col.Expression = expr
        col.DataType = DataType.Int64
        reg.Columns.Add(col)

        # per-capita measures
        s._upsert_measure(reg, "Region Population",
                          "SUM ( regions[Population] )", "#,0", "04 Geography",
                          "Total population of regions in context (Rosstat).")
        s._upsert_measure(reg, "Deaths per 100k (Confirmed)",
                          "DIVIDE ( SUM ( regions[total_deaths_full] ), SUM ( regions[Population] ) ) * 100000",
                          "0.0", "04 Geography",
                          "Confirmed deaths per 100,000 residents (authoritative regional totals).")
        model.SaveChanges()
        print("OK: Population column + per-capita measures created.")
    finally:
        srv.Disconnect()


if __name__ == "__main__":
    main()
